# O hash é parecido com o array, porém contém uma chave que serve para acessar o valor
#O hash tem uma key e o value


hash1 = {"a"=>"123", "b"=>"456"} # declarando um hash
puts hash1 # imprimindo todo o hash
puts hash1["a"] # imprimindo uma key específica do hash


hash2 = {a:"123", b: "456"} # declarando um hash utilizando símbolos
puts hash2 # imprimindo todo o hash
puts hash2[:a] # imprimindo uma key específica do hash