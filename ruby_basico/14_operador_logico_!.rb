# A ! (negação) serve para trocar o true pelo false e vice-versa
# Também é possível utilizar o not no lugar da !

numero1 = 1
numero2 = 2

# se o numero1 for menor que o numero2, faça a negação 
if !(numero1<numero2)
  # imprima este resultado "Resultado: True!" -> resultado para caso a negação seja true
  puts "Resultado: True!"
# caso contrário
else
  # e imprima este resultado "Resultado: False!"
  puts "Resultado: False!"
end