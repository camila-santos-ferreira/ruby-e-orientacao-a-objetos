# = -> atribuir valor
# += -> incrementar um valor
# -= -> decrementar um valor
# *= -> multiplicar a várivel por um valor e atribuir o resultado a própria variável
# /= -> dividir a várivel por um valor e atribuir o resultado a própria variável
# %= -> verificar o módulo da várivel em relação a um valor e atribuir o resultado a própria variável
# **= -> exponenciar a várivel por um valor e atribuir o resultado a própria variável

puts a = 2 # a = 2
puts a += 4 # a = a + 4 -> 6
puts a -= 4 # a = a - 4 -> 2
puts a *= 4 # a = a * 4 -> 8
puts a /= 4 # a = a / 4 -> 2
puts a %= 4 # a = a % 4 -> 2
puts a **= 4 # a = a ** 4 -> 16